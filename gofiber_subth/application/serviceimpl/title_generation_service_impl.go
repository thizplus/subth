func (s *TitleGenerationServiceImpl) GenerateTitle(ctx context.Context, userID uuid.UUID) (string, error) {
	stats, err := s.userStatsRepo.GetByUserID(ctx, userID)
	if err != nil {
		return "", err
	}
	if stats == nil {
		return levelTitles[1], nil
	}

	return s.GenerateTitleForLevel(ctx, stats.Level, stats)
}

